import User from "../models/user.model.js";
import bcryptjs from "bcryptjs";
import { errorHandler } from "../utils/error.js";
import jwt from "jsonwebtoken";

export const signup = async (req, res, next) => {
  const { username, email, password } = req.body;
  const hashedPassword = bcryptjs.hashSync(password, 10);
  const newUser = new User({ username, email, password: hashedPassword });
  try {
    await newUser.save();
    res.status(201).json("User created successfully!");
  } catch (error) {
    next(error);
  }
};

export const signin = async (req, res, next) => {
  const { email, password } = req.body;

  try {
    // check if there is a match of the requested email in the database
    const validUser = await User.findOne({ email: email });

    if (!validUser) {
      return next(errorHandler(404, "Use not found!"));
    }

    // if an valid email is found, check if the password is valid
    const validPassword = bcryptjs.compareSync(password, validUser.password);

    if (!validPassword) {
      return next(errorHandler(401, "Wrong Credentials!"));
    }

    console.log(validUser._id);

    // create a token with json web token library and add a cookie to the browser to temporarily save login information
    const token = jwt.sign({ id: validUser._id }, process.env.JWT_SECRET);
    // remove the password from the token that will be used to create the cookie as the best practice
    const { password: pass, ...rest } = validUser._doc;
    res
      .cookie("access_token", token, {
        httpOnly: true,
        expires: new Date(Date.now() + 60 * 60 * 1000),
      })
      .status(200)
      .json(rest);
  } catch (error) {
    next(error);
  }
};

